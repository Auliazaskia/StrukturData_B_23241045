# contoh tipe data (integar) : angka satuan 
bil_integer = 10001
print(type(bil_integer))
print ("type data dari bil_integer adalah :",type(bil_integer))

# contoh tipe data (float) : bilangan desimal 
bil_float = 2.5
print ("type data dari bil_integer adalah :",type(bil_float))

# contoh tipe data (string) : kumpulan karalter  
bil_string = "2.5"
print ("type data dari bil_integer adalah :",type(bil_string))

# contoh tipe data (bolean) : kumpulan biner, 0,1, True False 
bil_bol = False 
print ("type data dari bil_integer adalah :",type(bil_bol))

# tipe data dari bahasa c
from ctypes import c_double
bil_double = c_double(20000000)
print ("type data bil_double adalah: ", type(bil_double))
